#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned char	*i;

	if (n > 0)
	{
		i = s;
		while (n > 0)
		{
			*i = c;
			i ++;
			n --;
		}
	}
	return (s);
}
